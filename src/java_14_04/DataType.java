package java_14_04;

public class DataType {
    public static void main(String[] args) {
//        Integral
        byte num1=10;
        short num2=30000;
        int num3=400000000;
        long num4=2000000000;

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);

//        float/ Double

        float num5=23.678f; // remember to add a "f" at the end
        double num6=456.897;

        System.out.println(num5);
        System.out.println(num6);
//        Character

        char c1= 'A';
        char c2='a';

        System.out.println(c1);
        System.out.println(c2);

//        String / sentence

        String sen1="We are enjoying Java";
        System.out.println(sen1);
//        boolean

        boolean check1=true;
        System.out.println(check1);
        System.out.println();
        System.out.print("Mayukhjit");
    }
}
